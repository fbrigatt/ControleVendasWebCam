    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txfCpf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O cpf não pode ser vazio");
            txfCpf.requestFocus();
            return;
        }

        if (txfNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome não pode ser vazio");
            txfNome.requestFocus();
            return;
        }

        try {
            Cliente cliente = new Cliente();
            cliente.setCpf(txfCpf.getText());
            cliente.setNome(txfNome.getText());
            //RepositorioCliente.obterInstancia().inserir(cliente);
            this.clienteDao.insert(cliente);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

