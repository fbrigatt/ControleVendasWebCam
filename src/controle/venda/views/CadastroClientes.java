    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Cliente cliente = this.lista.get(jTable1.getSelectedRow());

        // mostra a showOptionDialog
        int choice = JOptionPane.showOptionDialog(null,
                "Deseja realmente excluir o cliente " + cliente.getNome() + "?",
                "Exclusão",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, null, null);

        // Botão SIM
        if (choice == JOptionPane.YES_OPTION) {
            try {
                this.clienteDao.remove(cliente.getCpf());
                JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Não foi possível excluir o Cliente! Possui venda cadastrada.");
                //JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        // Botão NÃO    
        } else {
            JOptionPane.showMessageDialog(null, "Operação cancelada!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

