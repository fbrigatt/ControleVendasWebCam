    public Cliente alteraPosicao(int proximaPosicao) throws SQLException, Exception {

        int totalRegistros = this.lista.size();

        if (proximaPosicao >= 0 && proximaPosicao < totalRegistros) {
            return this.lista.get(proximaPosicao);
        }

        if (proximaPosicao < 0) {
            // throw new Exception("Não já registro anterior!");
            JOptionPane.showMessageDialog(null, "Não há registro anterior!", "Alerta", JOptionPane.WARNING_MESSAGE);
            btnPrimeiro.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnProximo.setEnabled(true);
            btnUltimo.setEnabled(true);
        }

        if (proximaPosicao >= totalRegistros) {
            //throw new Exception("Não há proximo registro!");
            JOptionPane.showMessageDialog(null, "Não há proximo registro!", "Alerta", JOptionPane.WARNING_MESSAGE);
            btnProximo.setEnabled(false);
            btnUltimo.setEnabled(false);
            btnAnterior.setEnabled(true);
            btnPrimeiro.setEnabled(true);
        }

        return null;
    }

    private void mostrarDadoSelecionadoTabela() {
        int index = this.jTable1.getSelectedRow();

        this.clienteSelecionado = this.lista.get(index);
        this.posicao_atual = index;

        if (jTable1.getSelectedRow() != -1) {

            this.clienteSelecionado = this.lista.get(jTable1.getSelectedRow());
            txfCpf.setText(clienteSelecionado.getCpf());
            txfNome.setText(clienteSelecionado.getNome());
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma registro!", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    public void iniciarWebCam(){
        webSource = new VideoCapture(0);
        myThread = new CadastroClientes.DaemonThread();
        Thread t = new Thread(myThread);
        t.setDaemon(true);
        myThread.runnable = true;
        t.start();
        btnIniciar.setEnabled(false);  //botão Iniciar
        btnParar.setEnabled(true);  // botão Parar
    }
    
    public void pararWebCam(){
        myThread.runnable = false;
        btnParar.setEnabled(false);
        btnIniciar.setEnabled(true);

        webSource.release();
    }
    

